package day23encapsulationabstraction;

public abstract class Mammal extends Animal {

    /*
    Normally child class must override all "abstract methods" from the parent
    class. But Mammal class didn't override anything from the Animal class. and there
    is no complain. It's weird.

    Because overriding abstract methods from parent is mandatory just for concrete classes.
    Mammal is abstract class, so it is NOT mandatory to override.

    If you want, you can override abstract methods from parent to child class. It's optional;
    not mandatory.

     */


}
